    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {                                         
        // TODO add your handling code here:  
     ControladorRequerimientosReto4 controlador = new ControladorRequerimientosReto4();
        System.out.println("*** Lideres con mayor salario ***");     
        ArrayList<LideresMayorSalario> rankingLideresMayorSalario = null;
        try {
            rankingLideresMayorSalario = controlador.consultarLideresMayorSalarios();
        } catch (SQLException ex) {
            Logger.getLogger(ControladorRequerimientosReto4.class.getName()).log(Level.SEVERE, null, ex);
        }
        //Encabezado del resultado
        System.out.println("ID_Lider  Nombre  Primer_Apellido");     
       String printText = null;
       
        try (ByteArrayOutputStream outputStream = new ByteArrayOutputStream();
             PrintStream printStream = new PrintStream(outputStream, true))
        {
            for (LideresMayorSalario lideresMayors : rankingLideresMayorSalario) {
                 printStream.print("          "+lideresMayors.getIdLider());
                 printStream.print("   "+lideresMayors.getNombre());
                 printStream.println("       "+lideresMayors.getPrimerApellido());
           printText = outputStream.toString();
        }
        jTextArea1.setText("*** Lideres con mayor salario ***\n"
                 + "ID_Lider  Nombre  Primer_Apellido\n"
                    +printText);
        } catch (IOException ex) {
            Logger.getLogger(ControladorRequerimientosReto4.class.getName()).log(Level.SEVERE, null, ex);
        }
        for (LideresMayorSalario lideresMayors : rankingLideresMayorSalario) {
                 System.out.printf("      %d  %s   %s  %n",
                 lideresMayors.getIdLider(),
                 lideresMayors.getNombre(),
                 lideresMayors.getPrimerApellido()
         );  
        }    
    }  


    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {                                         
        // TODO add your handling code here:
        ControladorRequerimientosReto4 controlador = new ControladorRequerimientosReto4();
        System.out.println("*** Productos importados ***");       

        ArrayList<MaterialRankeadoImportado> rankingMaterialesImportados = null;
        try {
            rankingMaterialesImportados = controlador.consultarMaterialesRankeadosImportados();
        } catch (SQLException ex) {
            Logger.getLogger(ControladorRequerimientosReto4.class.getName()).log(Level.SEVERE, null, ex);
        }
        //Cada VO cargado, mostrarlo en la vista
        String printText3 = null;
       
        try (ByteArrayOutputStream outputStream = new ByteArrayOutputStream();
             PrintStream printStream = new PrintStream(outputStream, true))
        {
            for (MaterialRankeadoImportado materialImportado : rankingMaterialesImportados) {
                printStream.print("El producto de ID");
                 printStream.print(" "+materialImportado.getIdMaterial());
                 printStream.print(" de descripción:");
                 printStream.print(" "+materialImportado.getNombreMaterial());
                 printStream.print(" de tipo importado(a), tiene un precio de");
                 printStream.println(" "+materialImportado.getPrecioUnidad());
           printText3 = outputStream.toString();
        }
        jTextArea3.setText("*** Productos importados ***\n"
                    +printText3);
        } catch (IOException ex) {
            Logger.getLogger(ControladorRequerimientosReto4.class.getName()).log(Level.SEVERE, null, ex);
        }
        for (MaterialRankeadoImportado materialImportado : rankingMaterialesImportados) {
            System.out.printf("El producto de ID %d de descripción: %s de tipo importado(a), tiene un precio de %d %n",
                    materialImportado.getIdMaterial(),
                    materialImportado.getNombreMaterial(),
                    materialImportado.getPrecioUnidad()
            );
        }
    }             

